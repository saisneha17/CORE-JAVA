package com.dl.one;

public class Eg1 {

	public static void main(String[] args) {
		
		//Integer literal
		int a = 10;
		System.out.println(a);
		
		//here int is datatype
		//a is variable
		//= is operator
		//10 is constant value
		//; end of line
		
		//floating literal
		float fl= 10.0F;
		System.out.println(fl);
		
		double dl = 20.0d;
		System.out.println(dl);
		
		//Boolean Literals
		boolean bl = true;
		System.out.println(bl);
		
		//Character literals
		char ch ='A';
		System.out.println(ch);
		
		//string literals
		String name = "Sneha";
		System.out.println(name);
		
		//Binary literals
		int x = 0b0101;
		System.out.println(x);
		
		//Octal literals
		int y = 01234;
		System.out.println(x);	
		
		//Hexa literals
		int z = 0XEC12;
		System.out.println(z);
	}

}
